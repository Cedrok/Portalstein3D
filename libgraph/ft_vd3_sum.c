#include "libgraph.h"

t_vect_d3	ft_vd3_sum(t_vect_d3 a, t_vect_d3 b)
{
	t_vect_d3	s;

	s.x = a.x + b.x;
	s.y = a.y + b.y;
	s.z = a.z + b.z;
	return (s);
}
