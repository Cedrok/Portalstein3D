#include "libgraph.h"

double	distance_d2(t_vect_d2 a, t_vect_d2 b)
{
	double	d;

	d = sqrt((a.x - b.x) * (a.x - b.x) + (a.y - b.y) * (a.y - b.y));
	return (d);
}
